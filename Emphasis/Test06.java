package Notes.Emphasis;

/**
 * switch
 */
public class Test06 {
    public static void main(String[] args) {
        int num=1;
       // switch 使用的非常少  只能做等值判断
       switch (num){
           case 1:
               System.out.println("是等于1");
               break;
           case 2:
               System.out.println("是等于2");
               break;
           case 3:
               System.out.println("是等于3");
               break;
       }
        System.out.println("看执行了吗");
    }
}
